SELECT COUNT(*) FROM PRODUCT;
SELECT COUNT(*) as 카운트 FROM PRODUCT WHERE ID='D'; -- 없으면 0을 반환
SELECT COUNT(*) as 카운트 FROM PRODUCT WHERE ID='2'; -- 있으면 1을 반환

SELECT NAME,PRICE FROM PRODUCT WHERE PRICE BETWEEN 2000 AND 3000 ORDER BY PRICE DESC 

SELECT NAME,PRICE*0.9 as "할인가" FROM PRODUCT ORDER BY PRICE DESC

SELECT NAME,PRICE-(PRICE*10/100) as "할인가" FROM PRODUCT ORDER BY PRICE DESC